#include "main.h"
#include <stdlib.h>

/**
 * create_array - creates an array of chars,
 *		and initializes it with a specific char
 * @size: integer parameter showing size of array
 * @c: character
 * Return: character pointer
 **/

char *create_array(unsigned int size, char c)
{
	char *arr;

	if (size == 0)
		return (NULL);
	else
		return (arr[c]);
}
