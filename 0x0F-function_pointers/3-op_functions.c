#include "3-calc.h"

/**
 * op_add - returns the sum of a and b
 * @a: first integer
 * @b: second integer
 * Return: sum
 **/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference  of a and b
 * @a: first integer
 * @b: second integer
 * Return: subtraction
 **/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of a and b
 * @a: first integer
 * @b: second integer
 * Return: Multiplication
 **/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the division of a and b
 * @a: first integer
 * @b: second integer
 * Return: Division
 **/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division a and b
 * @a: first integer
 * @b: second integer
 * Return: Remainder of the division
 **/

int op_mod(int a, int b)
{
	return (a % b);
}
